    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DAO d = new DAO();
        HttpSession session = request.getSession();
        String rawid = request.getParameter("id");
        int id;
        try {
            id = Integer.parseInt(rawid);
            boolean deleteStatus = d.deleteCategories(id);
            if (deleteStatus) {
                session.setAttribute("msgc", "Successfully deleted Category with ID = " + id + "!");
                session.setAttribute("errc", "");
            } else {
                session.setAttribute("msgc", "");
                session.setAttribute("errc", "Unable to delete Category with ID = " + id + "!");
            }
        } catch (NumberFormatException e) {
            System.out.println(e);
        }
        response.sendRedirect("categoriesAdmin");
    }

